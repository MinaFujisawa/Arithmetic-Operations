public class Formula {
    int mNum_left;
    int mNum_right;
    String mOperation;
    int mNum_answer;

    public int getmNum_left() {
        return mNum_left;
    }

    public void setmNum_left(int mNum_left) {
        this.mNum_left = mNum_left;
    }

    public int getmNum_right() {
        return mNum_right;
    }

    public void setmNum_right(int mNum_right) {
        this.mNum_right = mNum_right;
    }

    public String getmOperation() {
        return mOperation;
    }

    public void setmOperation(String mOperation) {
        this.mOperation = mOperation;
    }

    public int getmNum_answer() {
        return mNum_answer;
    }

    public void setmNum_answer(int mNum_answer) {
        this.mNum_answer = mNum_answer;
    }
}
